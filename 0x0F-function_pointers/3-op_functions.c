#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - adds two ints
 * @a: firts int
 * @b: second int
 * Return: the sum of two ints
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract two ints
 * @a: firts int
 * @b: second int
 * Return: the substract of two ints
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two ints
 * @a: firts int
 * @b: second int
 * Return: the multiplicatiopn of two ints
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two ints
 * @a: firts int
 * @b: second int
 * Return: the div of two ints
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod of two ints
 * @a: firts int
 * @b: second int
 * Return: the mod of two ints
 */
int op_mod(int a, int b)
{
	return (a % b);
}
